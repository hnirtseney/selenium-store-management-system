package pos.form.settings;

public class CreateUnitForm {
    public String unitName;
    public String unitShortName;
    public String unitOperator;
    public String unitOperatorValue;


    // Construct0r
    public CreateUnitForm(String unitName, String unitShortName, String unitOperator, String unitOperatorValue) {
        this.unitName = unitName;
        this.unitShortName = unitShortName;
        this.unitOperator = unitOperator;
        this.unitOperatorValue = unitOperatorValue;
    }


}
